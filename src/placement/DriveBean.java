package placement;



public class DriveBean {

	String name, date, password;
	int status, technical, logical, verbal, aptitude;
	
	public String getname() {  
		return name ;  
	}  

	public void setname(String name) {  
		this.name = name;  
	}
	
	public String getdate() {  
		return date ;  
	}  

	public void setdate(String date) {  
		this.date = date;  
	}  
	
	public String getpassword() {  
		return password ;  
	}  

	public void setpassword(String password) {  
		this.password = password;  
	}
	
	public int getstatus() {  
		return status ;  
	}  

	public void setstatus(int status) {  
		this.status = status;  
	}  
	
	public int gettechnical() {  
		return technical ;  
	}  

	public void settechnical(int technical) {  
		this.technical = technical;  
	}  
	
	public int getlogical() {  
		return logical ;  
	}  

	public void setlogical(int logical) {  
		this.logical = logical;  
	}  
	
	public int getverbal() {  
		return verbal ;  
	}  

	public void setverbal(int verbal) {  
		this.verbal = verbal;  
	}  
	
	public int getaptitude() {  
		return aptitude ;  
	}  

	public void setaptitude(int aptitude) {  
		this.aptitude = aptitude;  
	}  
}
